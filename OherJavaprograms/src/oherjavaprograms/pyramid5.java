
package oherjavaprograms;


public class pyramid5
{
    public static void main(String[] args)
    {
        
        for(int j=0;j<5;j++)
        {
            for(int k=0;k<=j;k++)
            {
                System.out.print(k+1);
            }
            System.out.println();
        }
        
        for(int i=4;i>0;i--)
        {
            for(int j=0;j<i;j++)
            {
                System.out.print(j+1);
            }
            System.out.println();
        }
        
        
    }
}
