
package oherjavaprograms;


public class pyramid4 
{
    public static void main(String[] args)
    {
        
        for(int j=0;j<5;j++)
        {
            for(int k=0;k<=j;k++)
            {
                System.out.print(k+1);
            }
            System.out.println();
        }
        
        
        
    }
}
